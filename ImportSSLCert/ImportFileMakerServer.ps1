if ($DebugOn) {
	Write-Host "FileMaker Server Host Name: $ServerHostName"
	Write-Host "Certificate Path: $ImportCertPath"
	Write-Host "Keyfile Path: "$ImportKeyfilePath
	Write-Host "username: "$Username
	Write-Host "password: "$Password
}

$ImportCertCmd = "fmsadmin -y -u `"$Username`" -p `"$Password`" CERTIFICATE IMPORT `"$ImportCertPath`" --keyfile `"$ImportKeyfilePath`""
$RestartServerCmd = "fmsadmin -y -u `"$Username`" -p `"$Password`" restart server"

if ($DebugOn)
    {
    Write-Host $ImportCertCmd
    Write-Host $RestartServerCmd
	}
else {
	Invoke-Expression $ImportCertCmd
